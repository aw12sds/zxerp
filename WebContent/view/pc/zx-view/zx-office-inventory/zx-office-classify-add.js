var zxOfficeClassifyWinAdd;
var zxOfficeClassifyFormAdd;
function addZxOfficeClassify(){
	
	initZxMaterialClassifyFormAdd();
	zxOfficeClassifyWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:350,
		height:350,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:zxOfficeClassifyFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(zxOfficeClassifyFormAdd,'../zxOfficeClassifyController/addZxOfficeClassify',treePanel,zxOfficeClassifyWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	zxOfficeClassifyWinAdd.show();
	
}
