function addZxSupplier(){
	initFieldItems();
	initZxSupplierFormAdd();
	reGetWidthAndHeight();
	zxSupplierWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:clientWidth * 0.9,                    
		height:clientHeight * 0.9,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		resizable : false,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:zxSupplierFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm2(zxSupplierFormAdd,'../zxSupplierController/addZxSupplier',grid,zxSupplierWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	zxSupplierWinAdd.show();
	
}
