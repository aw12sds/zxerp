var zxWorkpieceWinAdd;
var zxWorkpieceFormAdd;
function addZxWorkpiece(){
	initZxWorkpieceFormAdd();
	zxWorkpieceWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:600,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:zxWorkpieceFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(zxWorkpieceFormAdd,'../zxWorkpieceController/addZxWorkpiece',grid,zxWorkpieceWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	zxWorkpieceWinAdd.show();
	
}
