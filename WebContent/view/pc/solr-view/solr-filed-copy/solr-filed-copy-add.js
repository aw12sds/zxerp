var solrFiledCopyWinAdd;
var solrFiledCopyFormAdd;
function addSolrFiledCopy(){
	initSolrFiledCopyFormAdd();
	solrFiledCopyWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:top.document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:solrFiledCopyFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(solrFiledCopyFormAdd,'../solrFiledCopyController/addSolrFiledCopy',grid,solrFiledCopyWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	Ext.getCmp('solr_core_id_').setValue($('#solr_core_id').val());
	solrFiledCopyWinAdd.show();
	
}
