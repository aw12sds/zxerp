var xtNoticeWinEdit;
var xtNoticeFormEdit;
function updateXtNotice(){
	var record = grid.getSelectionModel().selected;
	if(record.length == 0){
		msgTishi('请选择要修改的一项！');
		return;
	}
	initXtNoticeFormEdit();
	xtNoticeWinEdit = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'编辑信息',
		listeners:{
			minimize:function(win,opts){
				if(!win.collapse()){
					win.collapse();
				}else{
					win.expand();
				}
			}
		},
		items:xtNoticeFormEdit,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(xtNoticeFormEdit,'../xtNoticeController/updateXtNotice?systemUID='+record.items[0].data.xt_userinfo_id,grid,xtNoticeWinEdit,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	xtNoticeWinEdit.show();
	/**初始化附件右键菜单开始 参数4为1表示拥有上传和删除功能 即新增和编辑页面使用**/
	initFileRight('xt_attachment_id','xt_attachment_id_pic',1,1);
	initFileRight('xt_attachment_id_','xt_attachment_id__pic',1,1);
	/**初始化附件右键菜单结束**/

	/**配置附件回显方法开始**/
	var params = {xt_attachment_id:record.items[0].data.xt_attachment_id+','+record.items[0].data.xt_attachment_id_,field_name:'xt_attachment_id,xt_attachment_id_'};
	ajaxFilePathBackRequest('../xtCommonController/getAttachmentPathPP',params,1);
	/**配置附件回显方法结束**/
	loadFormData(xtNoticeFormEdit,'../xtNoticeController/getXtNoticeById?xt_notice_id='+ record.items[0].data.xt_notice_id);
}
