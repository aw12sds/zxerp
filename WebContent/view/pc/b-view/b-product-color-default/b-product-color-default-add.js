function addBProductColorDefault(){
	initBProductColorDefaultFormAdd();
	bProductColorDefaultWinAdd = Ext.create('Ext.Window',{
		layout:'fit',
		width:800,
		height:400,
		maximized:true,
		maximizable:true,
		minimizable:true,
		animateTarget:document.body,
		plain:true,
		modal:true,
		title:'添加信息',
		listeners:{
			minimize:function(win,opts){
				win.collapse();
			}
		},
		items:bProductColorDefaultFormAdd,
		buttons:[{
			text:'保存',
			itemId:'save',
			handler:function(button){
				submitForm(bProductColorDefaultFormAdd,'../bProductColorDefaultController/addBProductColorDefault',grid,bProductColorDefaultWinAdd,false,true);
			}
		},{
			text:'关闭',
			itemId:'close',
			handler:function(button){
				button.up('window').close();
			}
		}]
	});
	Ext.getCmp('b_product_id').setValue($('#b_product_id_').val());
	bProductColorDefaultWinAdd.show();
}
