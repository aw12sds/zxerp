package jehc.zxmodules.model;
import jehc.xtmodules.xtcore.base.BaseEntity;
import java.io.Serializable;

/**
* zx_office 办公用品管理 
* 2017-11-30 08:19:19  邓纯杰
*/
public class ZxOffice extends BaseEntity implements Serializable{
	private static final long serialVersionUID = 1L;
	private String office_id;/**办公用品ID**/
	private String name;/**办公用品名称**/
	private String num;/**办公用品编号**/
	private String classify_id;/**分类ID**/
	private String unit;/**度量衡，单位**/
	private String status;/**状态**/
	private String create_date;/**创建时间**/
	private String standard;/**规格型号**/
	public void setOffice_id(String office_id){
		this.office_id=office_id;
	}
	public String getOffice_id(){
		return office_id;
	}
	public void setName(String name){
		this.name=name;
	}
	public String getName(){
		return name;
	}
	public void setNum(String num){
		this.num=num;
	}
	public String getNum(){
		return num;
	}
	public void setClassify_id(String classify_id){
		this.classify_id=classify_id;
	}
	public String getClassify_id(){
		return classify_id;
	}
	public void setUnit(String unit){
		this.unit=unit;
	}
	public String getUnit(){
		return unit;
	}
	public void setStatus(String status){
		this.status=status;
	}
	public String getStatus(){
		return status;
	}
	public void setCreate_date(String create_date){
		this.create_date=create_date;
	}
	public String getCreate_date(){
		return create_date;
	}
	public void setStandard(String standard){
		this.standard=standard;
	}
	public String getStandard(){
		return standard;
	}
}
