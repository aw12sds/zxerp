package jehc.zxmodules.model;
import jehc.xtmodules.xtcore.base.BaseEntity;
import java.io.Serializable;

/**
* zx_meeting_house 会议室管理 
* 2017-11-17 15:51:33  邓纯杰
*/
public class ZxMeetingHouse extends BaseEntity implements Serializable{
	private static final long serialVersionUID = 1L;
	private String id;/**ID**/
	private String house_name;/**会议室名称**/
	private int max_num;/**容纳人数**/
	private String status;/**状态：1-可用，2-维修中**/
	private String state;/**状态：1-正常，0-删除**/
	private String remark;/**说明**/
	public void setId(String id){
		this.id=id;
	}
	public String getId(){
		return id;
	}
	public void setHouse_name(String house_name){
		this.house_name=house_name;
	}
	public String getHouse_name(){
		return house_name;
	}
	public void setMax_num(int max_num){
		this.max_num=max_num;
	}
	public int getMax_num(){
		return max_num;
	}
	public void setStatus(String status){
		this.status=status;
	}
	public String getStatus(){
		return status;
	}
	public void setState(String state){
		this.state=state;
	}
	public String getState(){
		return state;
	}
	public void setRemark(String remark){
		this.remark=remark;
	}
	public String getRemark(){
		return remark;
	}
}
